package Java;

public class loopsJava {
	
	public static void main(String[] args) {
	
		//for loop
		for(int i=0;i<=10;i++) {
			System.out.println(i);
	  }
		
		System.out.println("**************");
		//while loop
		int k = 1;
		while(k<10) {
			System.out.println(k);
			k++;
		}
		
		System.out.println("**************");
		
		//do..while loop
		int l = 1;
		do {
			System.out.println(l);
			l++;
		}while(l==10);
			
	}

}
